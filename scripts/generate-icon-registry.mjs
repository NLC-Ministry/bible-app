import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";
import { createRequire } from "node:module";

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const require = createRequire(pathToFileURL(root + "/"));
const iconLib = require("@icon-park/svg/lib/index.js");
const manifest = JSON.parse(readFileSync(join(root, "js/icon-manifest.json"), "utf8"));

const DEFAULTS = {
  theme: "outline",
  size: "24",
  fill: "currentColor",
  strokeWidth: 3,
  strokeLinejoin: "round",
  strokeLinecap: "round",
};

const registry = {};
const filledKeys = new Set(["heartFill", "likeFill"]);

for (const [key, iconName] of Object.entries(manifest)) {
  const factory = iconLib[iconName];
  if (typeof factory !== "function") {
    throw new Error(`generate-icon-registry: missing IconPark export ${iconName} for key "${key}"`);
  }
  const options = filledKeys.has(key)
    ? { ...DEFAULTS, theme: "filled" }
    : DEFAULTS;
  const svg = factory(options);
  if (!svg || !svg.includes("<svg")) {
    throw new Error(`generate-icon-registry: empty SVG for ${iconName} (${key})`);
  }
  registry[key] = svg.replace(/<\?xml[^?]*\?>\s*/i, "");
}

const out = `/* Auto-generated by scripts/generate-icon-registry.mjs — do not edit */\nwindow.NLC_ICON_SVGS = ${JSON.stringify(registry, null, 2)};\n`;
writeFileSync(join(root, "js/icon-registry.js"), out, "utf8");
console.log(`generate-icon-registry: wrote ${Object.keys(registry).length} icons to js/icon-registry.js`);
