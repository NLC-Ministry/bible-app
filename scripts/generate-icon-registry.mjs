import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { icons } from "lucide";

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const manifest = JSON.parse(readFileSync(join(root, "js/icon-manifest.json"), "utf8"));

const DEFAULTS = {
  width: 24,
  height: 24,
  stroke: "currentColor",
  strokeWidth: 2,
  fill: "none",
};

const filledKeys = new Set(["heartFill", "likeFill", "starFill", "zapFill"]);

function serializeAttrs(attrs) {
  return Object.entries(attrs)
    .map(([key, value]) => `${key}="${String(value).replace(/"/g, "&quot;")}"`)
    .join(" ");
}

function iconNodeToSvg(iconNode, options) {
  const opts = { ...DEFAULTS, ...options };
  const selfClosing = new Set(["path", "line", "polyline", "circle", "rect", "polygon", "ellipse"]);

  const children = iconNode
    .map(([tag, attrs]) => {
      const merged = { ...attrs };
      if (opts.filled) {
        if (tag === "path" || tag === "polygon") merged.fill = "currentColor";
        if (tag === "path" || tag === "polygon") merged.stroke = "currentColor";
      }
      const attrStr = serializeAttrs(merged);
      if (selfClosing.has(tag)) return `<${tag} ${attrStr}/>`;
      return `<${tag} ${attrStr}></${tag}>`;
    })
    .join("");

  return `<svg xmlns="http://www.w3.org/2000/svg" width="${opts.width}" height="${opts.height}" viewBox="0 0 24 24" fill="${opts.fill}" stroke="${opts.stroke}" stroke-width="${opts.strokeWidth}" stroke-linecap="round" stroke-linejoin="round">${children}</svg>`;
}

const registry = {};

for (const [key, iconName] of Object.entries(manifest)) {
  const iconNode = icons[iconName];
  if (!iconNode) {
    throw new Error(`generate-icon-registry: missing Lucide icon ${iconName} for key "${key}"`);
  }
  registry[key] = iconNodeToSvg(iconNode, { filled: filledKeys.has(key) });
}

const out = `/* Auto-generated by scripts/generate-icon-registry.mjs — do not edit */\nwindow.NLC_ICON_SVGS = ${JSON.stringify(registry, null, 2)};\n`;
writeFileSync(join(root, "js/icon-registry.js"), out, "utf8");
console.log(`generate-icon-registry: wrote ${Object.keys(registry).length} Lucide icons to js/icon-registry.js`);
